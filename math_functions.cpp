#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << pow(2, 6) << endl;
    cout << pow(2, 9.32) << endl;

    cout << sqrt(67) << endl;

    cout << round(4.6593) << endl;

    cout << floor(6.87) << endl;
    cout << ceil(2.0002) << endl;

    cout << fmax(2, 6) << endl;
    cout << fmin(2, 6) << endl;
}
